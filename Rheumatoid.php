      <br><h2>สารพันคำถามจากผู้ป่วย</h2></div>
      <strong>การรักษาโรครูมาตอยด์โดยใช้ผึ้งต่อยเจ็บไหม?</strong>
      <br/>
      <p>
      <u>ตอบ</u> การบำบัดด้วยผึ้งในครั้งแรกผู้ป่วยจะรู้สึกปวดบ้างในบางจุด แล้วแต่ลักษณะการใช้ผึ้งบำบัด ในครั้งต่อๆไป อาการปวดจากการต่อยผึ้งต่อยจะทุเลาลงค่ะ
      </p>
      <strong>โรครูมาตอยด์จะหายขาดหรือเปล่า?</strong>
      <br/>
      <p>
      <u>ตอบ</u> จากประวัติการรักษาพบว่าผู้ป่วยส่วนใหญ่อาการปวดทุเลาลงผลการตรวจเลือดพบว่าค่า<br>Rhumatoidfactor(RF)สามารถลดลงมาอยู่ในระดับปกติได้ค่ะ
      </p>
      <strong>หยุดยารักษาโรครูมาตอยด์จากโรงพยาบาลเลยได้ไหม?</strong>
      <br/>
      <p>
      <u>ตอบ</u> ให้ผู้ป่วยทานยาจากแพทย์ร่วมกับการรักษาโรครูมาตอยด์ไปก่อน หลังจากไม่พบการอักเสบ และค่าRF เป็นปกติแล้ว จึงปรึกษาแพทย์ประจำตัวท่านเพื่อขอหยุดยาได้ค่ะ
      </p>
