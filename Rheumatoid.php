      <div class="text1">
      โรครูมาตอยด์ 
      มักจะมีอาการปวดข้อพร้อมกันและคล้ายคลึงกันทั้ง 2 ข้าง มีการอักเสบ บวมแดงร้อน นิ้วมือนิ้วเท้าจะบวมเหมือนรูปกระสวย ต่อมาหากยังไม่ได้รับการรักษา อาการอักเสบจะลุกลามไปทุกข้อทั่วร่างกาย มีอาการปวดข้อและข้อแข็ง ติด อาการมักกำเริบมากในช่วงตื่นนอนหรือตอนเช้า ผลการตรวจเลือด จะพบว่าค่า Rheumatiod factor ได้ผล positive ตามทฤษฏีการแพทย์แผนจีนพบว่าโรครูมาตอยด์นั้นมีสาเหตุมาจากความเย็นชื้นหรือความร้อนสูงกระทบกับเส้นลมปราณ ทำให้เกิดการติดขัดของเลือดลม

      <p><strong>สาเหตุ </strong> ผลมาจากระบบภูมิคุ้มกันของร่างกายมีการตอบสนองอย่างผิดปกติต่อเชื้อโรค หรือสารเคมีบางอย่าง ทำให้มีการสร้างภูมิต้านทาน (แอนติบอดี) ที่มีปฏิกิริยาต่อเนื้อเยื้อในบริเวณข้อของตัวเองเรียกว่า ปฏิกิริยาภูมิต้านตนเอง (autoimmune)</p>
      <p><strong>รักษาโรครูมาตอยด์ด้วยผึ้งบำบัด</strong>โดยหลักการรักษาโรครูมาตอยด์ด้วยพิษผึ้งนั้นจะเน้นหนักเรื่องของการลดการอักเสบและเสริมสร้างระบบภูมิคุ้มกัน โดยในพิษผึ้งจะมีสารสำคัญ Apamin และ Adolapin ที่เข้าไปลดอาการอักเสบบริเวณเยื่อหุ้มข้อที่มีการดำเนินของโรค และในขณะเดียวกันนั้นพิษผึ้งก็ยังส่งผลก่อให้เกิดการอักเสบชั่วคราวจากผลของสาร Melittin และ Phospholipase A2 ทำให้บริเวณจุดที่ใช้พิษผึ้งรักษามีอาการบวมเล็กน้อย แต่ข้อดีคือเป็นการกระตุ้นให้เลือดมาเลี้ยงบริเวณนั้นเยอะขึ้น เพื่อเร่งการฟื้นฟูของเยื่อหุ้มข้อ กระดูกอ่อน และเนื้อเยื่อรอบๆที่มีความเสียหายจากการอักเสบ</p>
      <p><strong>ระยะเวลาในการรักษา</strong>  ในการรักษาโรครูมาตอยด์โดยวิธีผึ้งบำบัด ผู้ป่วยต้องมารับการรักษาต่อเนื่อง อย่างน้อยสัปดาห์ละ 1-3 ครั้ง โดยทั่วไปจะใช้ระยะเวลาในการรักษาอยู่ที่ประมาณ 1-3 เดือน ทั้งนี้ระยะเวลาการรักษานั้นขึ้นอยู่กับอายุของโรค รวมทั้งการดำรงชีวิตของผู้ป่วยด้วย หลังจากนั้นจะนัดผู้ป่วยมารับการรักษา ปีละประมาณ 3 ครั้ง เป็นระยะเวลา 2 ปี การรักษาโรครูมาตอยด์ของทางบิ๊กบีคลินิก</p>

      <p><strong>จัดแบ่งเป็นคอร์สได้ทั้งหมด3คอร์ส</strong>
      <br>คอร์สที่1(ปรับร่างกายบรรเทาอาการ)จำนวน15ครั้ง
      <br>คอร์สที่2(รักษา)จำนวน15ครั้ง
      <br>คอร์สที่ 3 (ฟื้นฟูร่างกาย) จำนวน 10 ครั้ง</p>
