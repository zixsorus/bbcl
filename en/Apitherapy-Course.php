      <p><strong>หลักการและเหตุผล</strong><br />
          <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;สมาคมผึ้งบำบัดไทย  (THAI APITHERAPY ASSOOCIATION)</strong>  เล็งเห็นถึงความสำคัญของศาสตร์ ผึ้งบำบัด  ซึ่งเป็นศาสตร์ทางเลือกที่มีความสนใจ และสามารถนำมาใช้ได้จริง  อีกทั้งยังเป็นศาสตร์ที่มีการใช้มาเป็นเวลานานนับพันปีและ  ปันจุบันมีการนำศาสตร์ผึ้งบำบัดไปใช้กันอย่างแพร่หลาย ทางสมาคมผึ้งบำบัดไทยจึงได้มีการจัดอบรมผึ้งบำบัดเบื้องต้นขึ้นเพื่อ  เผยแพร่ความรู้ด้านผึ้งบำบัด  ให้แก่แพทย์และบุคลากรทางการแพทย์ เพื่อใช้ในการบำบัดโรค  ตามแนวทางการแพทย์ทางเลือก และผู้สนใจที่จะใช้เพื่อการดูแลสุขภาพตนเอง และส่งเสริมประสิทธิภาพด้านการรักษาด้วยศาสตร์ผึ้งบำบัด </p>
      <p><strong>นิยามของผึ้งบำบัด</strong> <br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ผึ้งบำบัด หรือ Apitherapy หมายถึง  การบําบัด บรรเทา รักษาโรคและอาการโดยใช้ผลิตภัณฑ์ผึ้ง  อันได้แก่น้ำผึ้ง เกสรผึ้ง นมผึ้ง พรอพอริส  ไขผึ้ง และพิษผึ้ง  การรักษาโดยใช้ผึ้งมีมายาวนาน  และเป็นที่ยอมรับในระดับสากล วิธีการที่เรียกว่า <strong>“ผึ้งบําบัด”</strong> </p>
      <p><strong>ปรัชญาพื้นฐานของการบําบัด</strong>                        <br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ผึ้งบําบัด  (Apitherapy) มีแนวคิดหรือปรัชญาพื้นฐานในการใช้วิถีทางธรรมชาติในการบําบัดโรค ที่เรียกว่าอาหารเป็นยาก่อให้เกิดพลังต้านโรค เป็นการรักษาแบบองค์รวม  ไม่ได้มองแค่จุดที่เจ็บป่วยเพียงจุดเดียว  แต่มองว่าร่างกายทุกส่วนมีความสัมพันธ์และเกี่ยวโยงกันหมด ฉะนั้นการรักษาก็ต้องรักษาทั้งระบบ นํามาซึ่งความสมดุลในการบําบัดและทําให้ร่างกายทําหน้าที่อย่างมีประสิทธิภาพ    นอกจากนี้ ผึ้งบําบัด  (Apitherapy) ยังมุ่งเน้นการกระตุ้นพลังงานในร่างกายและเลือดลมให้ไหลเวียนได้ดีขึ้น เมื่อการไหลเวียนของโลหิตดีขึ้น  อาการเจ็บป่วยต่างๆ ก็ค่อยๆหายไป ซึ่งเป็นหลักการบําบัดรักษาโรคโดยวิถีทางธรรมชาติอย่างแท้จริง</p></div>
