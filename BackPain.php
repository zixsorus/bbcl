<p>1.ปวดแบบเฉียบพลัน จะทำการฝังเข็มด้วยเหล็กไนผึ้งร่วมกับฝังเข็ม และการครอบแก้ว เพื่อกระจายพิษอักเสบให้เลือดไหลเวียนมาเลี้ยงกล้ามเนื้อได้ดีขึ้น ร่วมกับการใช้สมุนไพรจีนเพื่อกระตุ้นการไหลเวียน</p>
