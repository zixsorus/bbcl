  <h2>ปวดคอ-บ่า (Neck and shoulder pain)</h2>
  <img src="images/neckpain.jpg" width="350px">
  </div>
  <div><p><b>“ปวดคอ ปวดบ่า ทานยา หายปวด หยุดยาอาการกำเริบ  <br>
เป็นหนักมากขึ้น ทานยาก็เอาไม่อยู่  ฉีดยาก็ไม่หาย ลองใช้วิธีผึ้งบำบัดดูซิครับ” </b>
</p></div>
  <div class="text1">
  <p>คนส่วนใหญ่มักเคยมีอาการปวดคอ บ่ากันมาบ้างแล้ว ซึ่งมักจะเริ่มจากอาการปวดเมื่อยต้นคอ เป็นๆหายๆ ซึ่งเกิดจากกล้ามเนื้อบริเวณบ่าและคอหดเกร็งจากการใช้งานที่ไม่ถูกวิธี หากอาการกำเริบมากขึ้น ผู้ป่วยมักจะมีอาการปวดร้าวบริเวณสะบัก ร้าวมาที่อกด้านหน้า ปวดลงต้นแขน ในบางรายที่เป็นที่ปล่อยเรื้อรังมากเข้ามักมีอาการปวดร้าวชาลงแขน  นอกจากนี้ยังส่งผลให้มีอาการปวดศีรษะ ซึ่งอาการปวดมักจะเริ่มปวดตึงจากท้ายทอย แล้วปวดลามไปที่ขมับข้างเดียวหรือปวดทั้งสองข้างก็ได้  ผู้ป่วยหลายคนมักจะซื้อยาแก้ปวดต่างๆ เช่น Ibuprofen หรือยาคลายกล้ามเนื้อ Diclofenacมาทานเอง โดยทั่วไปมักจะดีขึ้นเมื่อทานยา พอหยุดยาอาการปวดมักกำเริบขึ้นหนักกว่าเดิม
  </p>
<b>สาเหต</b>ุ<br>
<p>ส่วนใหญ่เกิดจากอริยาบถต่างๆในชีวิตประจำวัน เช่น การนั่งทำงานหรือใช้คอมพิวเตอร์เป็นเวลานานรวม  การนอนหมอนสูงเกินไป นั่งหลับเป็นเวลานาน ใช้คอหนีบโทรศัพท์ นอกจากนี้ยังอาจเกิดจากอุบัติเหตุซึ่งทำกล้ามเนื้อคอบิด สะบัด อย่างแรงและรวดเร็วทันที ซึ่งเรียกว่า วิพแลช (Whiplash) จะส่งผลให้เกิดการบาดเจ็บที่กล้ามเนื้อ เส้นเอ็น และข้อต่อที่คอทันที
</p>
<p>
  โรคและภาวะต่างๆที่ทำให้มีอาการปวดคอ และบ่า ได้แก่  ข้ออักเสบ(Arthritis) โรคกระดูกพรุน (Osteoporosis) โรคปวดกล้ามเนื้อ Fibromyalgia และหมอนรองกระดูกคอเคลื่อน (Herniated cervical disk) เป็นต้น
  </p>
<strong>รักษาอาการปวดคอ และบ่าด้วยผึ้งบำบัด</strong>
<br/>
<p>
   เริ่มต้นด้วยการใช้ศาสตร์การแพทย์ทางเลือก อาทิเช่น การจัดกระดูก ครอบแก้ว ฝังเข็ม หลังจากนั้นจึงใช้วิธีการฝังเข็มด้วยเหล็กในผึ้ง ตามจุดเจ็บ รวมทั้งจุดฝังเข็มตามแบบแพทย์จีน เริ่มแรกจะใช้ผึ้งประมาณ 1-3 ตัว หลังจากนั้นจึงเพิ่มปริมาณของผึ้งตามความเหมาะสม   โดยสารHistamineในพิษผึ้งจะก่อให้เกิดอาการปวด บวม แดง ร้อน ส่งผลให้เลือดไปเลี้ยงบริเวณที่มีอาการปวดมากขึ้น   ตรงกับหลักการรักษาโรคด้วยวิธี Homeopathy หรือพิษต้านพิษ คือวิธีการรักษาที่กระตุ้นระบบภูมิคุ้มกันในร่างกายของเราให้ต่อสู้กับโรคนั้นๆ  
</p>
<p>
    หลังจากผู้ป่วยเข้ารับการรักษาจะทำให้อาการปวดบริเวณคอ และบ่าลดลง สามารถเพิ่มองศาการเคลื่อนไหวบริเวณคอ และบ่าได้มากขึ้น รวมอาการปวดศีรษะ และอาการปวดร้าวชาลงแขนจะลดลง ในผู้ป่วยที่มีอาการเรื้อรังได้
</p>
<strong>ระยะเวลาในการรักษา</strong>
<br>
<p>
  ในการรักษาอาการปวดคอ และบ่าโดยวิธีผึ้งบำบัด ผู้ป่วยควรมารับการรักษาต่อเนื่อง อย่างน้อยสัปดาห์ละ 1-2 ครั้ง โดยทั่วไปจะใช้ระยะเวลาในการรักษาอยู่ที่ประมาณ 8-10 ครั้ง ทั้งนี้ระยะเวลาการรักษาจะขึ้นอยู่กับอายุของโรค รวมทั้งการดำรงชีวิตของผู้ป่วยด้วย 
</p>
<div align="center"><img src="images/question.jpg" width="350px">
<br><h2>สารพันคำถามจากผู้ป่วย</h2></div>

<strong>การรักษาอาการปวดคอ และบ่า โดยใช้ผึ้งต่อยเจ็บไหม?</strong>
<br/>
<p>
<u>ตอบ</u> การบำบัดด้วยผึ้งในครั้งแรกผู้ป่วยจะรู้สึกปวดบ้างในบางจุด แล้วแต่ลักษณะการใช้ผึ้งบำบัด ในการรักษาครั้งต่อๆไป อาการปวดจากการต่อยผึ้งต่อยจะทุเลาลงครับ
</p>
<strong>หากมีอาการปวดคอ บ่า สะบัก ร่วมกับอาการปวดชาลงแขน จะสามารถรักษาได้ไหม ?</strong>
<br/>
<p>
<u>ตอบ</u> จากประสบการณ์พบว่าผู้ป่วยส่วนใหญ่ที่มี อาการปวดคอ บ่า สะบัก และร้าวชาลงแขน มักมีอาการปวดคอ และบ่าเป็นๆหายๆ มานานมากกว่า 6 เดือน ซึ่งในกรณีนี้หากผู้ป่วยมารับการรักษาต่อเนื่อง ปฏิบัติตามคำแนะนำของหมอ ผลการรักษาในผู้ป่วยส่วนใหญ่ อาการปวดจะบรรเทาลงจนไม่พบอาการปวดเลย
</p>
