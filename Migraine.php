      <p class="test1"> ในปัจจุบันทางแพทย์แผนตะวันตกได้ชี้แจงว่า กลไกการเกิดโรคของไมเกรนนั้นเกิดจากความผิดปกติของสาร ”ซีโรโทนิน(Serotonin)” ที่ควบคุมเกี่ยวกับการหดและขยายตัวของหลอดเลือดแดง และเป็นสารที่เชื่อมโยงกับความเจ็บปวดจากทั่วร่างกายโดยตรง ดังนั้นเมื่อมีอาการปวดศีรษะไมเกรนจะรู้สึกว่าบริเวณที่ปวดมากจะมีความรู้สึกปวดตุบๆตามจังหวะชีพจร จึงอนุมานได้ว่าอาการปวดนั้นมาจากการขยายตัวที่มากเกินไปของหลอดเลือดแดงบริเวณศีรษะ จนไปส่งผลกระทบกับเส้นประสาทและเนื้อเยื่อโดยรอบบริเวณศีรษะจนเกิดอาการปวดตามมา ซึ่งมาจากการพร่องหรือเสียสมดุลของสาร “ซีโรโทนิน(Serotonin)”</p>
