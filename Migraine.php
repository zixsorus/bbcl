      <p>ยารักษาในปัจจุบันคือยาในกลุ่ม “Ergotamine” ซึ่งจะช่วยรักษาในช่วงเวลาที่มีอาการปวด
ไมเกรนเท่านั้น โดยจะทำให้หลอดเลือดแดงหดตัวลง ทำให้อาการปวดหายไปชั่วคราว จึงต้องทานทุกครั้งที่มีอาการปวด ซึ่งยังไม่ตอบโจทย์ในด้านการป้องกันโรคไมเกรนที่จะกลับมาปวดอยู่เสมอในอนาคต 
ซึ่งหากใช้ติดต่อกันเป็นเวลานานหรือไม่ระมัดระวังเรื่องโรคประจำตัว อาจส่งผลข้างเคียงที่อันตรายถึงชีวิต เช่น หลอดเลือดในสมองตีบ หัวใจขาดเลือด ความดันสูง ปลายมือปลายเท้าเน่า เป็นต้น
</p>
<p> หากแต่ปัจจุบันมีการค้นพบว่าในพิษผึ้งนั้นมีองค์ประกอบของสารชีวภาพหลากหลายชนิดรวมถึง
”เซโรโทนิน(Serotonin)”ที่มีคุณสมบัติสั่งการให้หลอดเลือดหดตัวลง จึงทำให้อาการปวดหัวลดลง 
ทั้งยังมีความสามารถในการต้านการอักเสบและลดอาการปวดได้เป็นอย่างดี 
ทางคลินิกจึงนำมาปรับประยุกต์ใช้เพื่อการบำบัดรักษาโรคไมเกรน และผนวกกับการรักษาแบบองค์รวมของแพทย์แผนทางเลือก ที่เข้ามามีบทบาทในการปรับสมดุลหยิน-หยาง ทะลวงเส้นลมปราณ และการระบายแกร่งเสริมพร่อง เพื่อให้ได้ผลการรักษาที่มีประสิทธิภาพ
</p>
